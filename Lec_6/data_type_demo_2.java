package Lec_6;

public class data_type_demo_2 {
	public static void main(String[] args) {
		if (1 < 2) {
			System.out.println("dsfsdfa");
		}
		boolean bb = false;
		if (bb) {
			System.out.println("nacho");
		}
		bb = true;
		if (bb) {
			System.out.println("cheesy nacho");
		}

		if (1 > 2 || bb) {
			System.out.println("dsfsdfa");
		}

		System.out.println("==============");
		System.out.println("Hello" + 10 + 20);
		System.out.println(10 + 20 + "Hello" + 10 + 20);
		System.out.println(10 + 20 + "Hello");

	}
}
